def print_params(a=1, b='строка', c=True):
    print(f'a: {a}, b: {b}, c: {c}')

# Вызовы функции с разным количеством аргументов
print_params()  # Вызов без аргументов
print_params(10)  # Вызов с одним аргументом
print_params(10, 'новая строка')  # Вызов с двумя аргументами
print_params(b=25)  # Вызов с изменением только одного именованного аргумента
print_params(c=[1, 2, 3])  # Вызов с изменением другого именованного аргумента
# Список с тремя элементами разных типов
values_list = [3.14, 'текст', False]

# Словарь с тремя ключами, соответствующими параметрам функции
values_dict = {'a': 42, 'b': 'слово', 'c': None}

# Передача списка и словаря в функцию с распаковкой
print_params(*values_list)  # Распаковка списка
print_params(**values_dict)  # Распаковка словаря
# Список с двумя элементами разных типов
values_list_2 = [54.32, 'Строка']

# Вызов функции с распаковкой списка и дополнительным аргументом
print_params(*values_list_2, 42)  # Распаковка списка и передача третьего аргумента
def print_params(a=1, b='строка', c=True):
    print(f'a: {a}, b: {b}, c: {c}')

# Шаг 1: Вызовы функции с разным количеством аргументов
print_params()  # Вызов без аргументов
print_params(10)  # Вызов с одним аргументом
print_params(10, 'новая строка')  # Вызов с двумя аргументами
print_params(b=25)  # Вызов с изменением только одного именованного аргумента
print_params(c=[1, 2, 3])  # Вызов с изменением другого именованного аргумента

# Шаг 2: Распаковка параметров
values_list = [3.14, 'текст', False]
values_dict = {'a': 42, 'b': 'слово', 'c': None}

print_params(*values_list)  # Распаковка списка
print_params(**values_dict)  # Распаковка словаря

# Шаг 3: Распаковка + отдельные параметры
values_list_2 = [54.32, 'Строка']
print_params(*values_list_2, 42)  # Распаковка списка и передача третьего аргумента